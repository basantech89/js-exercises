import lengthOfLastWord from './lastWordLength';

describe('lengthOfLastWord', () => {
  it('should return length of last word', () => {
    expect(lengthOfLastWord('Hello World')).toBe(5);
    expect(lengthOfLastWord('Hello World')).toBe(5);
    expect(lengthOfLastWord('Hello World')).toBe(5);
  });
});
